
public class Variable {
	private int block, size, s_num;
	private String data;
	public int getBlock() {
		return block;
	}
	public void setBlock(int block) {
		this.block = block;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int lexical) {
		this.s_num = lexical;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
}
